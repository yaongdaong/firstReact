function getGreeting(user) {
    // user 값을 받아서 인사말을 반환하는 함수
    if (user) {
        // user 객체가 존재할 경우 (null, undefined가 아닐 때)
        return <h1>Hello, {formatName(user)}! </h1>;
        // JSX를 반환
        // formatName(user) 함수를 호출해 사용자 이름을 화면에 출력
    }
    return <h1>Hello, Stranger.</h1>;
    // user가 없을 경우 기본 인사말을 담은 JSX 반환
}

