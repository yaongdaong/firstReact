// Welcome이라는 함수형 컴포넌트 정의
// props는 컴포넌트에 전달된 속성값들을 담고 있는 객체
function Welcome(props) {
    // props.name 값을 JSX 안에서 출력
    return <h1>안녕, {props.name}</h1>;
}
// Welcome 컴포넌트를 JSX로 생성
// name="인제" 값이 props 객체의 name으로 전달됨
const element = <Welcome name="인제" />;
// HTML 문서에서 id가 "root"인 DOM 요소를 가져와
// 리액트 루트 컨테이너를 생성 (React 18 방식)
const root = ReactDOM.createRoot(document.getElementById("root"));
// 리액트 루트에 element(Welcome 컴포넌트)를 렌더링
root.render(element);

