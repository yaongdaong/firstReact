class Hello extends React.Component {
    // React.Component를 상속받아 Hello라는 클래스형 컴포넌트를 정의
    render() {
        // 화면에 무엇을 그릴지 정의하는 render 메서드
        return <div>Hello {this.props.toWhat}</div>;
        // props로 전달받은 toWhat 값을 사용해 "Hello World" 같은 내용을 출력
    }
}
const root = ReactDOM.createRoot(document.getElementById("root"));
// id가 "root"인 DOM 요소를 찾아 React가 관리할 루트(root) 생성
root.render(<Hello toWhat="World" />);
// Hello 컴포넌트를 화면에 렌더링 하고, toWhat이라는 props에 "World"값을 전달

