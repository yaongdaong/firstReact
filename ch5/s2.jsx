// props 사용법: 키-값 쌍의 형태로 컴포넌트에 props를 전달함
function App(props) {
    return (
        // Profile 컴포넌트를 렌더링
        // name, introduction, viewCount라는 props를 전달
        <Profile
            name="소플" // 문자열 props (이름)
            introduction="안녕하세요, 소플입니다." // 문자열 props (소개글)
            viewCount={1500} // 숫자 props (조회수 → 중괄호 사용)
        />
    );
}

