// 함수 컴포넌트 (Function Component)
// props를 매개변수로 받아서 JSX를 반환하는 가장 기본적인 컴포넌트 형태
function Welcome(props) {
    return <h1>안녕, {props.name}</h1>; // 전달받은 props.name 사용
}
// 클래스 컴포넌트 (Class Component)
// 모든 클래스 컴포넌트는 React.Component를 상속받아야 한다
class Welcome extends React.Component {
    // render() 메서드는 반드시 필요하며
    // 화면에 보여줄 JSX를 반환한다
    render() {
        return <h1>안녕, {this.props.name}</h1>; // this.props로 props 접근
    }
}

