const name = "소플";
// 화면에 출력할 이름을 문자열 변수로 저장
const element = <h1>안녕, {name}</h1>;
// JSX 문법으로 React 엘리먼트 생성
// {name} 부분에 name 변수 값이 들어가서 "안녕, 소플"이 됨
const root = ReactDOM.createRoot(document.getElementById("root"));
